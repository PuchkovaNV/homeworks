my_string = input('Введите два Ваших любимых цвета (через пробел): ')
print('Количество символов: ', len(my_string))
print('Большими буквами: ', my_string.upper())
print('Маленькими буквами: ', my_string.lower())
print('Без пробелов: ', my_string.replace(' ',''))
print('Первая буква: ', my_string[0])
print('Последняя буква: ', my_string[-1])

